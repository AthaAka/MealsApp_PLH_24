    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        int row = this.jComboBox1.getSelectedIndex();
        if(row>0){
            String name = MainClass.listOfCategories.get(row-1).getName();
            String url = "https://www.themealdb.com/api/json/v1/1/filter.php?c="+name;
            String data = JSonAnalyzer.getJSonData(url);
            ArrayList<Meal> list = JSonAnalyzer.getInformationAboutMeals(data, name);
            fillData(tableModel, list);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    
    private void fillData(DefaultTableModel model, ArrayList<Meal> list){ 
        model.setRowCount(0);
        for(Meal m:list){
            Vector grammi = new Vector();
            grammi.add(m.getStrmeal());       
            model.addRow(grammi);
        }
    }

